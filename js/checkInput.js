const checkInputNumber = (e) => {
  e.value = e.value.replace(/[^0-9.]/g, '').replace(/(\..*?)\..*/g, '$1')
  if (e.value.length > 2) {
    e.value = e.value.slice(-2)
  }
  if (e.value > 0 || e.value.length > 1) {
    e.value = e.value * 1
  }
}

